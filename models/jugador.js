const { Schema, model } = require("mongoose");

const jugadorSchema = Schema({
  nombre: {
    type: String,
    required: true,
  },
  userName: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  img: {
    type: String,
  },
  role: {
    type: String,
    required: true,
    default: "USER_ROLE",
  },
});

module.exports = model("Jugador", jugadorSchema);
